      DO i2 = LBOUND(OutData%twoDArray,2), UBOUND(OutData%twoDArray,2)
        DO i1 = LBOUND(OutData%twoDArray,1), UBOUND(OutData%twoDArray,1)
          OutData%twoDArray(i1,i2) = REAL(ReKiBuf(Re_Xferred), SiKi)
          Re_Xferred = Re_Xferred + 1
